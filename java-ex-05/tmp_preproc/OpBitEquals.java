class OpBitEquals {

public static void main(String args[]) {

int a = 1;

int b = 2;

int c = 3;

a |= 4;

b >>= 1;

c <<= 1;

a ^= c;

char []__wrap_const_string1={'a',' ','=',' '};



System.out.println((new String(__wrap_const_string1) ) + a);

char []__wrap_const_string2={'b',' ','=',' '};


System.out.println((new String(__wrap_const_string2) ) + b);

char []__wrap_const_string3={'c',' ','=',' '};


System.out.println((new String(__wrap_const_string3) ) + c);
} }

