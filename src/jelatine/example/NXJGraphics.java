    void lines()
    {
        displayTitle("Lines");
        for (int i = 1; i < SH / 2; i += 4)
        {
            g.drawLine(i - 4, i, SW - i, i);
            g.drawLine(SW - i, i, SW - i, SH - i);
            g.drawLine(i, SH - i, SW - i, SH - i);
            g.drawLine(i, i + 4, i, SH - i);
        }
        g.drawLine(0, 0, SW, SH);
        g.drawLine(SW, 0, 0, SH);
        Button.waitForAnyPress(DELAY);
    }

